def plus(val1,val2):
    result = val1 + val2
    print(result)

def minus(val1, val2):
    result = val1 - val2
    print(result)

def multiple(val1, val2):
    result = val1 * val2
    print(result)

def devide(val1,val2):
    result = val1 / val2
    print(result)

plus(10,20)
minus(10,20)
multiple(10,20)
devide(10,20)
